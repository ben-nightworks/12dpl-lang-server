import { describe, expect, test, beforeAll } from "bun:test";
import { spawnSync } from "node:child_process";
import { existsSync, readFileSync, statSync } from "node:fs";
import { join } from "node:path";

const repoRoot = join(import.meta.dir, "..");
const websiteRoot = join(repoRoot, "website");
const publicDir = join(websiteRoot, "public");
const buildScript = join(websiteRoot, "scripts", "build_site_data.py");
const siteDataJs = join(publicDir, "site_data.js");
const dataDir = join(publicDir, "data");
const indexHtml = join(publicDir, "index.html");

function pythonCmd(): string {
	for (const candidate of ["python", "python3", "py"]) {
		const r = spawnSync(candidate, ["--version"], { encoding: "utf8" });
		if (r.status === 0) return candidate;
	}
	return "python";
}

describe("website build", () => {
	let buildStdout = "";

	beforeAll(() => {
		const result = spawnSync(pythonCmd(), [buildScript], {
			cwd: repoRoot,
			encoding: "utf8",
		});
		buildStdout = (result.stdout || "") + (result.stderr || "");
		if (result.status !== 0) {
			throw new Error(`build_site_data.py failed:\n${buildStdout}`);
		}
	});

	test("build script writes site_data.js and mirrors raw JSON", () => {
		expect(existsSync(siteDataJs)).toBe(true);
		expect(statSync(siteDataJs).size).toBeGreaterThan(100_000);

		for (const f of ["functions.enriched.json", "typeDocumentation.json", "snippets.json"]) {
			const p = join(dataDir, f);
			expect(existsSync(p)).toBe(true);
			expect(statSync(p).size).toBeGreaterThan(0);
		}
	});

	test("site_data.js exposes window.SITE_DATA with the expected shape", () => {
		const js = readFileSync(siteDataJs, "utf8");
		expect(js).toStartWith("// Generated by");
		expect(js).toInclude("window.SITE_DATA = ");

		const jsonStart = js.indexOf("{");
		const jsonEnd = js.lastIndexOf("}");
		expect(jsonStart).toBeGreaterThan(0);
		expect(jsonEnd).toBeGreaterThan(jsonStart);
		const payload = JSON.parse(js.slice(jsonStart, jsonEnd + 1));

		expect(payload.stats).toBeDefined();
		expect(payload.stats.functions).toBeGreaterThan(1000);
		expect(payload.stats.types).toBeGreaterThan(100);
		expect(payload.stats.snippets).toBeGreaterThan(0);

		expect(Array.isArray(payload.functions)).toBe(true);
		expect(Array.isArray(payload.types)).toBe(true);
		expect(Array.isArray(payload.snippets)).toBe(true);
		expect(payload.functions.length).toBe(payload.stats.functions);
		expect(payload.types.length).toBe(payload.stats.types);
		expect(payload.snippets.length).toBe(payload.stats.snippets);

		const f0 = payload.functions[0];
		expect(typeof f0.name).toBe("string");
		expect(typeof f0.signature).toBe("string");
		expect(typeof f0.category).toBe("string");

		const t0 = payload.types[0];
		expect(typeof t0.name).toBe("string");
		expect(typeof t0.group).toBe("string");

		const s0 = payload.snippets[0];
		expect(typeof s0.label).toBe("string");
		expect(typeof s0.body).toBe("string");
	});

	test("build script logs the expected counts", () => {
		expect(buildStdout).toInclude("functions :");
		expect(buildStdout).toInclude("types     :");
		expect(buildStdout).toInclude("snippets  :");
	});
});

