describe("prism-12dpl grammar", () => {
	test("registers a self-contained 12dpl language", () => {
		const src = readFileSync(join(publicDir, "prism-12dpl.js"), "utf8");
		const stub: any = {
			Prism: { languages: {} },
			document: {
				readyState: "complete",
				addEventListener() { /* no-op */ },
			},
		};
		// The script reads `window.Prism` — provide it via the function's `this`/scope.
		const fn = new Function("window", "document", "Prism", src);
		fn(stub, stub.document, stub.Prism);

		const lang = stub.Prism.languages["12dpl"];
		expect(lang).toBeDefined();
		expect(stub.Prism.languages["12dPL"]).toBe(lang);

		for (const key of ["comment", "string", "class-name", "keyword", "boolean", "function", "number", "operator", "punctuation", "macro"]) {
			expect(lang[key]).toBeDefined();
		}

		const classNameRe = lang["class-name"] as RegExp;
		for (const t of ["Text", "Integer", "Real", "Element", "Model", "Panel", "Vector3", "void"]) {
			expect(t).toMatch(classNameRe);
		}
		const fnRe = lang["function"] as RegExp;
		expect("To_text(").toMatch(fnRe);
		expect("Print(").toMatch(fnRe);
		expect("not_a_call").not.toMatch(fnRe);

		const kwRe = lang["keyword"] as RegExp;
		for (const k of ["if", "else", "for", "while", "return", "persistent"]) {
			expect(k).toMatch(kwRe);
		}
	});
});

describe("SEO meta tags", () => {
	let html = "";
	beforeAll(() => {
		html = readFileSync(indexHtml, "utf8");
	});

	test("includes description, keywords, robots, theme-color", () => {
		expect(html).toMatch(/<meta\s+name="description"\s+content="[^"]+12dPL[^"]*"/);
		expect(html).toMatch(/<meta\s+name="keywords"\s+content="[^"]+"/);
		expect(html).toMatch(/<meta\s+name="robots"\s+content="[^"]*index, follow[^"]*"/);
		expect(html).toMatch(/<meta\s+name="googlebot"/);
		expect(html).toMatch(/<meta\s+name="bingbot"/);
		expect(html).toMatch(/<meta\s+name="theme-color"/);
	});

	test("includes canonical, sitemap link, and favicon variants", () => {
		expect(html).toMatch(/<link\s+rel="canonical"\s+href="https:\/\/[^"]+"/);
		expect(html).toMatch(/<link\s+rel="sitemap"\s+type="application\/xml"\s+href="\/sitemap\.xml"/);
		expect(html).toMatch(/<link\s+rel="apple-touch-icon"/);
	});

	test("includes Open Graph tags", () => {
		for (const prop of ["og:type", "og:site_name", "og:title", "og:description", "og:url", "og:image"]) {
			expect(html).toInclude(`property="${prop}"`);
		}
	});

	test("includes Twitter card tags", () => {
		for (const name of ["twitter:card", "twitter:title", "twitter:description", "twitter:image"]) {
			expect(html).toInclude(`name="${name}"`);
		}
	});

	test("includes JSON-LD structured data", () => {
		const m = html.match(/<script type="application\/ld\+json">([\s\S]*?)<\/script>/);
		expect(m).not.toBeNull();
		const parsed = JSON.parse(m![1]);
		expect(parsed["@context"]).toBe("https://schema.org");
		expect(Array.isArray(parsed["@graph"])).toBe(true);
		const types = parsed["@graph"].map((n: any) => n["@type"]);
		expect(types).toContain("WebSite");
		expect(types).toContain("SoftwareSourceCode");
		expect(types).toContain("TechArticle");
	});
});

describe("robots.txt and sitemap.xml", () => {
	test("robots.txt allows everything and lists AI bots explicitly", () => {
		const robotsPath = join(publicDir, "robots.txt");
		expect(existsSync(robotsPath)).toBe(true);
		const robots = readFileSync(robotsPath, "utf8");

		expect(robots).toMatch(/^User-agent:\s*\*\s*$/m);
		expect(robots).toMatch(/^Allow:\s*\/\s*$/m);

		for (const bot of ["GPTBot", "ClaudeBot", "anthropic-ai", "CCBot", "Google-Extended", "PerplexityBot", "Bytespider"]) {
			expect(robots).toInclude(`User-agent: ${bot}`);
		}

		expect(robots).toMatch(/^Sitemap:\s*https?:\/\/.+\/sitemap\.xml\s*$/m);
		expect(robots).not.toMatch(/^Disallow:\s*\/\s*$/m);
	});

	test("sitemap.xml is well-formed and lists key sections", () => {
		const sitemapPath = join(publicDir, "sitemap.xml");
		expect(existsSync(sitemapPath)).toBe(true);
		const xml = readFileSync(sitemapPath, "utf8");

		expect(xml).toStartWith('<?xml version="1.0" encoding="UTF-8"?>');
		expect(xml).toInclude('xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"');
		expect(xml).toInclude("<urlset");
		expect(xml).toInclude("</urlset>");

		const locs = [...xml.matchAll(/<loc>([^<]+)<\/loc>/g)].map(m => m[1]);
		expect(locs.length).toBeGreaterThan(20);
		expect(locs.some(l => l.endsWith("/"))).toBe(true);
		expect(locs.some(l => l.includes("#about"))).toBe(true);
		expect(locs.some(l => l.includes("#all-calls"))).toBe(true);
		expect(locs.some(l => l.includes("#snippets"))).toBe(true);
	});
});
